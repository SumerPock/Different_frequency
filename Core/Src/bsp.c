unsigned short do_crc_table(unsigned char *ptr, int len)
{
    unsigned short crc = 0xFFFF;
    
    while(len--) 
    {
        crc = (crc >> 8) ^ crc_table[(crc ^ *ptr++) & 0xff];
    }
    
    return (crc);
}


/**
  * @brief  Uart_State_Machine
  * @retval int
  */
void Uart_State_Machine(struct UARTState_Machine* UMa , struct LM1266* lm)
{
//	struct LM1266 mylm1266 = {0};
	unsigned char us8[40] = {0};
	unsigned short crc1 = 0;
	unsigned short crc2 = 0;
	switch(UMa->ucStatus){
		/* 状态0保证接收到 */
		case 0:
			if(UMa->read == 0x01){
				UMa->ucStatus = 1;        
			}
		break;			
		/* 状态1保证接收到 */
		case 1:
			if(UMa->read == 0x03){
					UMa->ucStatus = 2;
				}else{
					UMa->ucStatus = 0;
			}
		break;	
		
		case 2:
			UMa->buf[UMa->ucCount] = UMa->read;
			UMa->datalen = UMa->buf[0];
			/* 接收够32个数据 */
			if(UMa->datalen + 2 == UMa->ucCount){
//				comSendBuf(COM1, (uint8_t *)(UMa->buf), 7);
				for(unsigned char i = 0 ; i <= UMa->datalen  + 2; i++){
					us8[i + 2] = UMa->buf[i];
				}
				/*Data res*/
				us8[0] = 0X01;
				us8[1] = 0X03;
//			us8[2] = 0X20;
				crc1 = do_crc_table(us8, 35);
				crc2 = us8[36]<<8 | us8[35];
				if(crc1 == crc2){
					UMa->ucStatus = 0;
					UMa->ucCount = 0;	
					/*数据读取正确*/
					UMa->UMflag = 1;
					lm->number = us8[2];
					lm->voltage = (us8[3]<<24) | (us8[4]<<16) | (us8[5]<<8) | us8[6];
					lm->current = (us8[7]<<24) | (us8[8]<<16) | (us8[9]<<8) | us8[10];
					lm->power = (us8[11]<<24) | (us8[12]<<16) | (us8[13]<<8) | us8[14];
					lm->electrical_energy = (us8[15]<<24) | (us8[16]<<16) | (us8[17]<<8) | us8[18];
					lm->Power_Factor = (us8[19]<<24) | (us8[20]<<16) | (us8[21]<<8) | us8[22];
					lm->carbon_dioxide = (us8[23]<<24) | (us8[24]<<16) | (us8[25]<<8) | us8[26];
					lm->frequency = (us8[30]<<24) | (us8[31]<<16) | (us8[32]<<8) | us8[33];
					/*启动事件标志位*/
					osEventFlagsSet(event_lm1266_ID , 0x01U);
				}
				else{
					/*数据校验错误*/
					for(unsigned char i =0  ; i < 48 ; i++){
						UMa->buf[i] = 0;
					}
					UMa->ucStatus = 0;
					UMa->ucCount = 0;
					UMa->UMflag = 0;
				}
			}
			else{
				UMa->ucCount++;
		}
		break;	
			
		default:
		break;
	}
}


